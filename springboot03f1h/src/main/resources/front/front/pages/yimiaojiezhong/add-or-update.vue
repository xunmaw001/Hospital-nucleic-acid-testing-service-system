					this.$utils.msg(`接种余位不能为空`);
					return
				}
												if(this.ruleForm.jiezhongyuwei&&(!this.$validate.isIntNumer(this.ruleForm.jiezhongyuwei))){
					this.$utils.msg(`接种余位应输入整数`);
					return
				}
																																																																																																																																																																																																																								if(this.ruleForm.id){
					await this.$api.update(`yimiaojiezhong`, this.ruleForm);

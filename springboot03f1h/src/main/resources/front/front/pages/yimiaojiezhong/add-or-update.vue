					continue;
					}
					if(o=='jiezhongyuwei'){
					this.ruleForm.jiezhongyuwei = obj[o];
					this.ro.jiezhongyuwei = true;
					continue;
					}
					if(o=='yihugonghao'){
					this.ruleForm.yihugonghao = obj[o];
					this.ro.yihugonghao = true;
					continue;
					}
					if(o=='yihuxingming'){
					this.ruleForm.yihuxingming = obj[o];
					this.ro.yihuxingming = true;
					continue;
					}
